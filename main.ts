function mudarCena (gridCols: number) {
    GridCols = gridCols
    if (jogador.tilemapLocation().column <= 0) {
        mapa = mapa - 1
        tiles.setCurrentTilemap(maps[mapa])
        jogador.setPosition(230, jogador.y)
    }
    if (jogador.tilemapLocation().column >= 15) {
        mapa = mapa + 1
        tiles.setCurrentTilemap(maps[mapa])
        jogador.setPosition(20, jogador.y)
    }
    if (jogador.tilemapLocation().row <= 0) {
        mapa = mapa - GridCols
        tiles.setCurrentTilemap(maps[mapa])
        jogador.setPosition(jogador.x, 230)
    }
    if (jogador.tilemapLocation().row >= 15) {
        mapa = mapa + GridCols
        tiles.setCurrentTilemap(maps[mapa])
        jogador.setPosition(jogador.x, 20)
    }
}
function mudarDeMapa (selectMapa: number) {
    mapa = selectMapa
    tiles.setCurrentTilemap(maps[mapa])
}
