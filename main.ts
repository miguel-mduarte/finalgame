function itemColect () {
    if (jogador.overlapsWith(item)) {
        item.setPosition(item.x, item.y - 5)
        pause(100)
        item.setPosition(item.x, item.y - 5)
        pause(100)
        item.setPosition(item.x, item.y + 5)
        pause(100)
        item.setPosition(item.x, item.y + 5)
        pause(500)
        game.splash("voce encontrou ", itemName)
        if (game.ask("Coletar item?")) {
            item.changeScale(-0.2, ScaleAnchor.Middle)
            pause(70)
            item.changeScale(-0.2, ScaleAnchor.Middle)
            pause(70)
            item.changeScale(-0.2, ScaleAnchor.Middle)
            pause(200)
            sprites.destroy(item)
            game.splash("voce adquiriu", itemName)
        } else {
            while (jogador.overlapsWith(item)) {
                pause(300)
                item.setFlag(SpriteFlag.Ghost, true)
                itemColect()
                item.setFlag(SpriteFlag.Ghost, false)
            }
        }
    }
}
